def concot(name, surname, year, city, email, fon): # функция
    print(name, ' ', surname,' ', year,' ', city,' ', email,' ', fon) #тут мы конкотинацию делаем

#Вводим данные
name = input('Введите имя ')
surname = input('Введите фамилию ')
year = input('Введите год рождения ')
